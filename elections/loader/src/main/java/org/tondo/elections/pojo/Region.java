public class Region {
	
	private String name;
	
	private Double votersCoef;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Double getVotersCoef() {
		return votersCoef;
	}

	public void setVotersCoef(Double votersCoef) {
		this.votersCoef = votersCoef;
	}
	
	

}
